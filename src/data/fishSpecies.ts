// --- HELPER FUNCTIONS ---

// Helper function to get a single species by its ID
export const getSpeciesById = (id: string): FishSpecies | undefined => {
  return fishSpeciesData.find(species => species.id === id);
};

// Helper function to get all species belonging to a specific family
export const getSpeciesByFamily = (family: string): FishSpecies[] => {
  return fishSpeciesData.filter(species => species.family === family);
};

// Helper function to get a sorted list of all unique family names
export const getAllFamilies = (): string[] => {
  const families = fishSpeciesData.map(species => species.family);
  // Using Set to get unique values, then converting back to an array and sorting
  return [...new Set(families)].sort();
};

// Helper function to get a sorted list of all unique habitats
export const getAllHabitats = (): string[] => {
  const habitats = fishSpeciesData.map(species => species.habitat);
  return [...new Set(habitats)].sort();
};

// Helper function to get all species with a specific conservation status
export const getSpeciesByConservation = (status: FishSpecies['conservation']): FishSpecies[] => {
  return fishSpeciesData.filter(species => species.conservation === status);
};